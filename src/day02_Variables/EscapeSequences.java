package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("\t\t\t\t\t\tHello");
        System.out.println("\tHello");
        //this means paragraph

        System.out.println("Cybertek School");
        System.out.println("Cybertek \n School");
        System.out.println("\n\nMy\nName\nIs\nAkmammet");
        // \n means new line every time

        System.out.println("My favorite TV show is \"Game of Thrones\"");
        System.out.println("My favorite book is \"Changing the future with coding\"");
        // \" means double quote. need to close it with \"

        System.out.println("\\");
        // \\ prints single slash on the print statement
        System.out.println("\'Java\'");
        // \'print one single quote
    }
}
